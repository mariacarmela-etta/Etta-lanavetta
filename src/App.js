// --- FUNZIONE DI RICERCA INTELLIGENTE ---
const normalizeForSearch = (text) => {
  if (!text) return '';
  return text
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/duemila/g, "2000")
    .replace(/citta/g, "citta")
    .replace(/[^a-z0-9\s]/g, " ")
    .trim();
};

const matchesQuery = (stopName, query) => {
  if (!query) return true;
  const normStop = normalizeForSearch(stopName);
  const qWords = normalizeForSearch(query).split(/\s+/).filter(Boolean);
  return qWords.every(w => normStop.includes(w));
};


